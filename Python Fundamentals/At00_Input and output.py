# 输出: print()在括号中加上字符串就能在屏幕上输出指定的文字。
#       例如: print('hello,world')
#       print也可以打印整数，或者计算结果:
#       例如: print(300)    print(100+300)
# 输入: 例如: name = input()

# Output Examples:
print('hello,world')
print('The quick brown fox', 'jumps over', 'the lazy dog.')
print(300)
print('100 + 200 =', 100 + 200)

# Input Examples:

name = input()
print('hello,', name)


